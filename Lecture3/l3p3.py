#wapp to read Four number and print minimum and maximum of Four numbers using built in functions

n1=float(input("Enter the First number= "))
n2=float(input("Enter the Second number= "))
n3=float(input("Enter the Third number= "))
n4=float(input("Enter the forth number= "))


minres=min(n1,n2,n3,n4)
maxres=max(n1,n2,n3,n4)


print("Minimum result=",minres)
print("Maxmum result=",maxres)
