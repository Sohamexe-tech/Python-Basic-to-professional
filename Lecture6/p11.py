#wapp to demo adv type initialization 

#list is ordered collection of mutable elements
d1=[10,30,20,40,20,10]
print(d1,type(d1))

#tuple is ordered collection of immutable elements 
d2=(10,30,20,40,20,10)
print(d2,type(d2))

#set is unordered collection of mutable unique element 
d3={10,30,20,40,20,10}
print(d3,type(d3))

#dict is ordered collection of mutable key:value pair
d4={"Soham ":40,"Sumit":56 ,"neha":60}
print(d4,type(d4)) 